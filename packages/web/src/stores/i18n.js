export const useI18nStore = defineStore('i18n', {
  state: () => ({
    locale: localStorage.getItem('huhaa-locale') || 'zh',
  }),
  getters: {
    t: (state) => (key) => messages[state.locale]?.[key] || messages.zh[key] || key,

    // Get localized skill property (name, description, category)
    skillText: (state) => (skill, field) => {
      if (!skill) return '';
      if (state.locale === 'zh') {
        const zhField = `${field}_zh`;
        if (skill.i18n?.[zhField]) return skill.i18n[zhField];
      }
      return skill[field] || '';
    },
  },
  actions: {
    setLocale(locale) {
      this.locale = locale === 'en' ? 'en' : 'zh';
      localStorage.setItem('huhaa-locale', this.locale);
      document.documentElement.lang = this.locale === 'zh' ? 'zh-CN' : 'en';
    },
    toggle() {
      this.setLocale(this.locale === 'zh' ? 'en' : 'zh');
    },
  },
});
